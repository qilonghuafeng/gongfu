package gongfu;

public class Jiuyangshengong {
	private String id;
	private String name;
	private String property;
	public Jiuyangshengong() {
		super();
	}
	public Jiuyangshengong(String id, String name, String property) {
		super();
		this.id = id;
		this.name = name;
		this.property = property;
	}
	@Override
	public String toString() {
		return "Jiuyangshengong [id=" + id + ", name=" + name + ", property=" + property + "]";
	}
	

}
